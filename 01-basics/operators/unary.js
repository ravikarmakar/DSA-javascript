// Unary Operators:-
//  ++  - Increment,  variants (pre-increment, post-increment)
//  --  - Decrement,  variants (pre-decrement, post-decrement)

// post-increment
// let a = 10;
// let b = a++;

// console.log(a); // 11
// console.log(b); // 10

// pre-increment
// let a = 10;
// let b = ++a;

// console.log(a); // 11
// console.log(b); // 11

// post-decrement
// let a = 10;
// let b = a--;

// console.log(a); // 9
// console.log(b); // 10

// pre-increment
let a = 10;
let b = --a;

console.log(a); // 9
console.log(b); // 9
